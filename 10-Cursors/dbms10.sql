1] CURSOR PROGRAM FOR ELECTRICITY BILL CALCULATION

create table Electricity049 (Name varchar2(15), Prev_Reading number(5), Curr_Reading number(5));
INSERT INTO Electricity049 VALUES ('Shushrut Kumar', 2590, 5891);
INSERT INTO Electricity049 VALUES ('Viren Parmar', 7555, 10210);
INSERT INTO Electricity049 VALUES ('Vidhi Rai', 8190, 11203);

set serveroutput on;
DECLARE
CURSOR bill
IS
SELECT * FROM Electricity049;
ROW_ELECTRICITY ELECTRICITY049%ROWTYPE;
AMOUNT NUMBER(9,2);
BEGIN
OPEN bill;
LOOP
FETCH bill INTO ROW_ELECTRICITY;
EXIT WHEN bill%NOTFOUND;
AMOUNT := ( ROW_ELECTRICITY.CURR_READING - ROW_ELECTRICITY.PREV_READING ) * 5;
DBMS_OUTPUT.PUT_LINE('BILL FOR ' || ROW_ELECTRICITY.NAME || ' is Rs ' || AMOUNT);
END LOOP;
CLOSE bill;
END;
