2] CURSOR PROGRAM FOR STUDENT GRADE CALCULATION

create table STUDENT049 (Name varchar(20), Maths number(3), Science number(3));
INSERT INTO STUDENT049 VALUES ('Shushrut Kumar', 79, 84);
INSERT INTO STUDENT049 VALUES ('Viren Parmar', 99, 88);
INSERT INTO STUDENT049 VALUES ('Sachin Tilokani', 34, 65);
INSERT INTO STUDENT049 VALUES ('Neel Thakar', 67, 55);
INSERT INTO STUDENT049 VALUES ('Param Shah', 49, 46);

set serveroutput on;
DECLARE
CURSOR grade
IS
SELECT * FROM STUDENT049;
STUDENT_ROW STUDENT049%ROWTYPE;
BEGIN
OPEN grade;
LOOP
FETCH grade INTO STUDENT_ROW;
EXIT WHEN grade%NOTFOUND;
IF (STUDENT_ROW.MATHS + STUDENT_ROW.SCIENCE) > 100 THEN
DBMS_OUTPUT.PUT_LINE(STUDENT_ROW.NAME || ' has achieved grade PASS with total marks = ' || (STUDENT_ROW.MATHS + STUDENT_ROW.SCIENCE));
ELSE
DBMS_OUTPUT.PUT_LINE(STUDENT_ROW.NAME || ' has achieved grade FAIL with total marks = ' || (STUDENT_ROW.MATHS + STUDENT_ROW.SCIENCE));
END IF;
END LOOP;
CLOSE grade;
END;
